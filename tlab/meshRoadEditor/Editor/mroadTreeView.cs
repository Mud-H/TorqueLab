function MeshRoadTreeView::onSelect(%this, %obj) {
	MeshRoadEditorGui.road = %obj;
	MeshRoadInspector.inspect( %obj );

	if(%obj != MeshRoadEditorGui.getSelectedRoad()) {
		MeshRoadEditorGui.setSelectedRoad( %obj );
	}
}
